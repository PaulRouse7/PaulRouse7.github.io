function search(str) {        
	let results = [];
	for(let fruits of fruit){
		let bigFruit = fruits.toUpperCase();
		if(bigFruit.includes(str) && str !== ''){
			results.push(fruits);
		}
	}
	return results;
}

input.addEventListener('keyup',searchHandler);           
function searchHandler(e) {
	suggestions.innerHTML = '';
	let results = search(e.target.value.toUpperCase());  
	for(let result of results){
		let fruit = document.createElement('li');
		fruit.innerText = result;
		suggestions.appendChild(fruit);
	}
}


function showSuggestions(results, inputVal) {           
	searchHandler();
	search();
}

suggestions.addEventListener('click',useSuggestion)
function useSuggestion(e) {              
	let fruit = e.target.innerText;
	input.value = fruit;
}
